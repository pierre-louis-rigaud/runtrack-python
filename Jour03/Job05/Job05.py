    if operator == '+':
        print(num1+num2)
    elif operator == '-':
        print(num1-num2)
    elif operator == '*':
        print(num1*num2)
    elif operator == '/':
        print(num1/num2)
    elif operator =='%':
        print(num1%num2)
    return print

